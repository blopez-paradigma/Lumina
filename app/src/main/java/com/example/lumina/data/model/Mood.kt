package com.example.lumina.data.model

/**
 * Represents the various emotional states a user can track within the Lumina application.
 *
 * This enum defines a spectrum of moods ranging from positive high-energy states
 * to reflective or negative emotional states, used for monitoring mental well-being.
 */
enum class Mood {
    HAPPY,
    CALM,
    PEACEFUL,
    EXCITED,
    GRATEFUL,
    REFLECTIVE,
    TIRED,
    SAD,
    ANXIOUS,
    ANGRY
}
